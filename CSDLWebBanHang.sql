
CREATE TABLE ChucDanh(
	MaCD INT PRIMARY KEY,
	TenCD NVARCHAR(50),
	TrangThai INT
);

CREATE TABLE LOAITAIKHOAN 
(
MALTK INT PRIMARY KEY,
TENLOAI NVARCHAR(30),
TRANGTHAI INT
);

CREATE TABLE TAIKHOAN 
(
MATK INT PRIMARY KEY,
MALTK INT,
USERNAME VARCHAR(30),
PASS VARCHAR(30),
TRANGTHAI INT,
FOREIGN KEY (MALTK) REFERENCES LOAITAIKHOAN (MALTK)
);

CREATE TABLE NHANVIEN
(
	MaNV INT PRIMARY KEY,
	Ho NVARCHAR(10),
	TenLot NVARCHAR(10),
	Ten NVARCHAR(30),
	NgaySinh DATE,
	Phai NVARCHAR(3),
	CCCD VARCHAR(12),
	SDT VARCHAR(10),
	DiaChi NVARCHAR(100),
	NgayBDLam DATE,
	NgayKetThuc DATE,
	MaCD INT,
	MaNV_QL INT,
	MaTK INT,
	TrangThai INT,
	FOREIGN KEY(MaTK) REFERENCES TAIKHOAN (MATK),
	FOREIGN KEY(MaCD) REFERENCES ChucDanh (MaCD),
	FOREIGN KEY(MaNV_QL) REFERENCES NhanVien(MaNV)
);

CREATE TABLE THUONGHIEU
(
MATHUONGHIEU INT PRIMARY KEY,
TENTHUONGHIEU VARCHAR(60),
SDT VARCHAR(20),
TRANGTHAI INT
);

CREATE TABLE LOAISANPHAM
(
MALOAI INT PRIMARY KEY,
TENLOAI VARCHAR(30),
PHAI VARCHAR(3),
TRANGTHAI INT
);
 
CREATE TABLE SANPHAM
(
MASP INT ,
TENSP VARCHAR(100),
MATHUONGHIEU INT NOT NULL,
MALOAI INT NOT NULL,
SOLUONG INT,
TINHTRANG INT,
CHATLIEU VARCHAR(50),
GIA INT,
SIZE INT,
HINHANH VARCHAR(50),
PRIMARY KEY(MASP,SIZE),
FOREIGN KEY (MATHUONGHIEU) REFERENCES THUONGHIEU (MATHUONGHIEU),
FOREIGN KEY (MALOAI) REFERENCES LOAISANPHAM (MALOAI)
);

CREATE TABLE KHACHHANG
(
SDT VARCHAR(10) PRIMARY KEY,
TENKH VARCHAR(50) ,
DIACHI VARCHAR(100) ,
MAIL VARCHAR(50),
MaTK INT,
TRANGTHAI INT,
FOREIGN KEY(MaTK) REFERENCES TAIKHOAN (MATK)
);

CREATE TABLE HOADON
(
MAHD INT PRIMARY KEY,
NGAYLAPHD DATE,
MANV INT,
SDT VARCHAR(10),
TRANGTHAI INT,
FOREIGN KEY (MANV) REFERENCES NHANVIEN (MANV),
FOREIGN KEY (SDT) REFERENCES KHACHHANG (SDT)
);

CREATE TABLE CT_HOADON
(
MAHD INT,
MASP INT,
SIZE INT,
SOLUONG INT,
GIA INT,
CHIETKHAU FLOAT,
PRIMARY KEY(MAHD,MASP,SIZE),
FOREIGN KEY (MAHD) REFERENCES HOADON (MAHD),
FOREIGN KEY (MASP,SIZE) REFERENCES SANPHAM (MASP,SIZE)
);



CREATE TABLE SALE
(
MASALE INT PRIMARY KEY,
NGAYBATDAU DATE,
NGAYKETTHUC DATE,
TINHTRANG INT
);

CREATE TABLE CT_SALE
(
MASALE INT,
MASP INT,
SIZE INT,
CHIETKHAU FLOAT,
PRIMARY KEY(MASALE,MASP,SIZE),
FOREIGN KEY (MASALE) REFERENCES SALE (MASALE),
FOREIGN KEY (MASP,SIZE) REFERENCES SANPHAM (MASP,SIZE)
);
CREATE TABLE DONHANG
(
MADON INT,
SDT VARCHAR(10),
NGAYDAT DATE,
NGAYGIAO DATE,
TRANGTHAI INT,
PRIMARY KEY(MADON),
FOREIGN KEY (SDT) REFERENCES KHACHHANG (SDT)
);
CREATE TABLE CT_DONHANG
(
MADON INT,
MASP INT,
SIZE INT,
GIA INT,
SOLUONG INT,
PRIMARY KEY(MADON,MASP,SIZE),
FOREIGN KEY (MADON) REFERENCES DONHANG (MADON),
FOREIGN KEY (MASP,SIZE) REFERENCES SANPHAM (MASP,SIZE)
);
CREATE TABLE LUOTTRUYCAP
(
SOLUONG INT
);
CREATE TABLE BINHLUAN
(
MABL INT,
MASP INT,
SIZE INT,
MATK INT,
NOIDUNG VARCHAR(150),
THOIGIAN DATETIME,
TRANGTHAI INT,
PRIMARY KEY(MABL,MASP,SIZE,MATK),
FOREIGN KEY (MASP,SIZE) REFERENCES SANPHAM (MASP,SIZE),
FOREIGN KEY(MaTK) REFERENCES TAIKHOAN (MATK)
);
